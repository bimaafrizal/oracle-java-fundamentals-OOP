package Segitiga;

public class segitiga {
	private int tinggi;
	private int alas;
	
	public segitiga(int tinggi, int alas)
	{
		this.tinggi = tinggi;
		this.alas = alas;
	}
	public void setAlas(int newAlas)
	{
		alas = newAlas;
	}
	public void setTinggi(int setTinggi)
	{
		tinggi = setTinggi;
	}
	public int getAlas()
	{
		return alas;
	}
	public int getTinggi()
	{
		return tinggi;
	}

}
